package schema

import (
	"github.com/neko-sc/ent"
	"github.com/neko-sc/ent/schema/field"

	"github.com/google/uuid"
)

// Item holds the schema definition for the Item entity.
type Item struct {
	ent.Schema
}

// Fields of the Item.
func (Item) Fields() []ent.Field {
	return []ent.Field{
		field.String("id").
			DefaultFunc(uuid.NewString).
			MaxLen(64),
		field.String("text").
			MaxLen(128).
			Unique().
			Optional(),
	}
}
